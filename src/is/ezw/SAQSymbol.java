package is.ezw;

/**
 * Symbol codes.
 */
public class SAQSymbol {
	/** The "zerotree" and "zero" symbol. */
	public static final int ZTR = 0;

	/** The "isolated zero" symbol. */
	public static final int IZ = 1;

	/** The "significant positive" symbol. */
	public static final int POS = 2;

	/** The "significant negative" symbol. */
	public static final int NEG = 3;

	private SAQSymbol() {}
}
